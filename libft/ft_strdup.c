#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*string;
	int		i;

	i = 0;
	string = ft_calloc(ft_strlen(s) + 1, 1);
	while (s[i] != '\0')
	{
		string[i] = s[i];
		i++;
	}
	string[i] = '\0';
	return (string);
}
