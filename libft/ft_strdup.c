char *ft_strdup(const char *s)
{
	char	*string;
	int		i;

	i = 0;
	string = malloc(ft_strlen(s));
	while (s[i] != '\0')
	{
		string[i] = s[i];
		i++;
	}
	string[i] = '\0';
	return (string);
}