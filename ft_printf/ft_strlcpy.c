#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	lenght;

	i = 0;
	lenght = 0;
	if (size != 0)
	{
		while (src[i] != '\0' && i < size - 1)
		{
			dst[i] = src[i];
			i++;
			lenght++;
		}
		dst[i] = '\0';
	}
	while (src[lenght] != '\0')
		lenght++;
	return (lenght);
}
